<p>そんなある意味、相続する田畑があるからこその、惰性的に継続されてきた職業である「農業」ですが、時代の変遷とともに「農業に対する再評価」の高まりが、昨今の資源高やウクライナ侵略戦争を要因として「食糧危機」という文字が踊りつつ、世界中の人々が危機感をもって考え、農業を再評価するような環境が整いつつあります。</p>

<p>これまで当然にわが口に入るはずだった「食料」としての野菜が、「安い高い」に関係なく手に入らないような状況に陥る可能性を、現実味のある問題として世界中の人々が認識をし、前述したような「本音の適正価格」を維持してあげなければ、わが国の農業については「お先真っ暗」であり、これからも農業を継続できない農家は増え続けることになるでしょう。</p>

<p>しかるに、「農業の再定義」とは、今の農家が専業農家として、経済的に食べていけるだけの所得向上が必要であり、みなさまが「本音としての野菜価格の上昇を許容する」ための意識改革が先決であり、そんな環境が整えば「大事な食料を生産してくれる農家を支える」という再定義に対する意識も生まれてくると考えています。</p>

<p>現在すでに60代以上の方々は、子どものころ「お百姓さんが苦労して作ったお米を一粒たりとも残さず食べなさい！」という、農業を営む人々への敬意をもって、親から叱咤叱責を受けた経験のある方々ならわかるとおり、実は世界中の人々が「農業者へ敬意を示す時代」に突入しているということでもあります。</p>
