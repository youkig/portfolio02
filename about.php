<div class="block">
	<h3>【太陽と野菜の直売所】が目指す農業とは</h3>

<ul>
	<li>化学肥料を使わない昔ながらの有機栽培農法</li>
<li>「見た目」だけを維持するための農薬類は使わない</li>
<li>最新の土壌化学を基礎として、適正な微生物資材を活用する</li>
				<li>野菜の味覚的価値を消費者に広く認知させる</li>
				<li>姿形の良好でない野菜の差別的な販売価格を付けない</li>
				<li>新規就農者が職業として容易に参入できる生産マニュアルの体系化</li>
				<li>農場で使用する電力はソーラー自家発電で賄い、環境負荷にやさしい農業とする</li>
				<li>IoT技術を駆使した人材の省力化（自動水やりなど）</li>
				<li>化石燃料を一切使わない農業ハウス内冷暖房システムの開発</li>
				<li>野菜価値の上昇に寄与する第三の価値観の構築</li>
</ul>

<p>概ねこんな感じですが、10番目の「野菜価格の上昇」を目指すという意味は、「今の野菜価格が安すぎるから」が根拠であり、有機栽培である（おいしい）無農薬（安全性）のほかに、「次の価値」となる何かを模索するということでもあります。</p>

<p>この点においては、全責任は生産者側にあり、例えば「無農薬」と表示しているにもかかわらず、実はたっぷり農薬が使用されているとか、「有機栽培」の表示が真っ赤なウソで、化学肥料だけで育てられたものであったりと、消費者が「美味＋安心安全」を疑いなく買うことのできる、その信用性を裏付ける「何か」（第三の価値）なのかも知れません。</p>

<p>わたくし岡本自身が直売所の店頭に立ち、来店していただいたお客様方々に、直接生産現場となる農地を見て、また試食もしていただき、納得の上で野菜の価値を決めていただけるような農産物直売所にしたいと思っています。</p>

<p>そんな思いに賛同いただける方々に、ぜひ一度農場見学に来ていただけると幸いです。</p>

