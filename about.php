<p>わたくしは、東京・両国駅すぐ隣に、やっちゃ場（野菜市場：江東分場）が昭和48年ごろまで営業していたことを記憶していますが、それ以前の農家は肥料として人糞を多用しており、すでに化学肥料はあったものの高価で、人糞散布が禁止されるまで安価な肥料として施用されていました。</p>

<p>かつて、そんな市場周辺の野菜仲卸問屋の看板に「洗浄野菜」という文字が目立つようになりましたが、それはすなわち、畑の土を含むそんな汚物肥料を「きれいに洗って販売しています」という付加価値であり、次第に「見た目のきれいな、姿かたちのよい野菜」に消費者が指向していったことが発端になっています。</p>

<p>このときは、昭和の高度成長期と重なり、わが国が敗戦国としての貧困の時代から解放されていった時代でもありました。</p>
