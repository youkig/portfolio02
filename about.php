<div class="block">
	<h3>化学肥料の出現について</h3>

<p>前項に書いたとおり、戦後から高度成長期までの肥料といえば主に人糞であり、一部の大規模農家では、化学肥料の施用が価格的にも合理的であるという理由で早い段階から使われましたが、地方農家では人糞肥料すら高価で使えず、落ち葉、枯葉を集めて馬糞、牛糞、鶏糞、豚糞、油粕、魚粕などを利用して自家製の肥料を作って栽培していたのです。</p>

<p>そして、一般的に化学肥料（正確には「化学合成肥料」）が使われ多用され始めたのは、この時代の高度成長期であり、この間今日まで「たった45年程度」でしかありません。</p>

<p>但し、ここで大事なことは、「8－8－8」「14－14－14」という、窒素、リン酸、カリウム成分それぞれが「8％」「14％」含有という化学肥料には、基本的に植物の生育に必要な3大元素成分しかなく、ほかアミノ酸類、カルシウム、鉄、ホウ素、硫黄、モリブデンなどの微量必須元素が存在せず、結果として「野菜本来の味ではない」「病害に弱い」「姿形だけが似て異なもの」が生産され続けたということになります。</p>

<p class="catch">要するに、化学肥料の出現で、農家が野菜の栽培方法を「忘れて（失くして）」しまったのです。</p>
</div>



<div class="block">
	<h3>たった45年で失われた有機栽培技術</h3>

<p>すなわち、化学肥料の出現で、農業の王道ともいえる有機栽培が徐々に衰退し、自家製肥料（ぼかし肥等）の作り方、多種多様な野菜の種類に対する肥料成分の混合方法など、施肥技術の伝承も止まってしまいました。</p>

<p>もちろん、人糞に代わる有機肥料資材は多く流通していましたが、自分の農地を持たない小作人などの貧農と呼ばれた人たちには、到底手の届かない高根の花でした。</p>

</div>


